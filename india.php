	<div id="right-bar">
		<h3>laXmi</h3>
		<p><em>Fall 2011</em></p>
		<p><em><a href="https://github.com/Cryclops/IndiaTablet" target="_blank">GitHub</a></em></p>
		<br />
		<p>IndiaTablet (name to be improved) is an Android application designed for a class at
		Virginia Tech called <em>Design for the Bottom of the Pyramid</em>.
		The idea is to combine multiple disciplines with the common goal of
		designing and developing something that will help the developing
		world.</p>
		<br />
		<p>I joined a group that was picking up after a project named
		<em>laXmi</em> that developed a teaching aid for women interested
		in learning more about microfinancing techniques in India. They
		identified and addressed several problems they found with the learning
		process - chiefly they provided a visual way to simulate the impact of
		interest rates over time. You can learn more about the project
		<a href="http://www.id4learning.com/laxmi.html" target="_blank">here,
		as well as watch a video of me explaining the app</a>. I don't give much
		background on India's microfinancing techniques here, so please forgive
		me if some of the follow is unclear.</p>
		<br />
		<p>Our group re-evaluated the issues and how the current design
		addresses them. We decided that the <em>laXmi</em> solution
		efficiently solved a few issues, but ignored others. We focused on a
		select few:</p>
		<ul>
			<li>Groups tend to keep the habit of holding on to their pooled
			money once it becomes sizable. This is a bad habit since money not
			loaned is money not earning interest. Case studies show that the
			most successful microfinancing groups make a point to loan out as
			much as possible.</li>
			<li>Groups are often discouraged after the first year because they
			barely break even (subsequent years are exponentially better). The
			<em>laXmi</em> simulation partially helps with that but falls
			short due to there being too many numbers to keep track of. Writing
			down transactions by hand becomes unmanagable during
			simulations.</li>
		</ul>
		<br />
		<h4>Design</h4>
		<br />
		<p>After several iterations, we came up with the idea of using India's
		new
		<a href="http://aakashtablet.org/" target="_blank">Aakash tablet</a>
		as a tool to help the facilitator teach in combination with the current
		<em>laXmi</em> solution. The NGO facilitator uses our application as a tool
		to keep track of groups and simulations.</p>
		<br />
		<p>Before, facilitators had to write transactions down and keep
		accounting by hand - distracting them from the fast paced simulation in
		front of them. Now, they can simply follow along with the meetings,
		keeping track with a few clicks. Calculations are done for them and
		the software runs an idealistic side-by-side account of how they group
		could improve.</p>
		<br />
		<h4>Future</h4>
		<br />
		<p>According to our teacher, the <em>laXmi</em> system has been
		officially adopted by the
		<a href="http://wcd.nic.in/" target="_blank">Ministry of Women and Child
		Development</a>. It is a finalist in the
		<a href="http://worlddesignimpact.org/projects/project/35/" target="_blank">World
		Design Impact competition</a>. My work is open source on GitHub and it
		is our hope that the community will pick it up and improve it!</p>
