			<h4>Rocker and Vacuum Bagging</h4>
			<br />
			<p>There's a lot of debate on what rocker, or curve, is best for a
			board. I read as much as I could and went with an average of what
			I'd read. I also decided to laminate via vacuum bagging, a process
			that bonds materials together in a bag under pressure, so I had to
			build a rocker table. The table is an inverse mold of the board's
			bottom -- made out of supports and 8mm Luan from Home Depot.</p>
		</div>
