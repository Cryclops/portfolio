			<h4>Technique</h4>
			<br />
			<p>Foam comes off like butter, but the wooden stiffener (called a
			stringer) down the center does not. It requires either block sanding
			or a scary planing tool that will eat the foam for dinner if it
			touches.</p>
			<br />
			<p>Lamination is done with quick setting polyester resin. You cut
			out the proper sections of fiberglass cloth, mix the resin, then
			pour and spread it. You only have about 10-15 minutes to do an
			entire side of the board, which leads to high risk. Wrinkle the
			cloth or drip everywhere and you'll either have a hell of a mess or
			you'll destroy the board. To prepare for this step, we practiced in
			a variety of ways. First by reading techniques, trying to understand
			the reasons behind the steps described, then by working with small
			sample pieces of foam to see how the materials behaved. This process
			worked pretty well; we came out of the lamination phase with only a
			few headaches.</p>
